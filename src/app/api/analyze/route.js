import Anthropic from '@anthropic-ai/sdk'

const client = new Anthropic({ apiKey: process.env.ANTHROPIC_API_KEY })

export async function POST(request) {
  const { name, logs, profile, mode } = await request.json()

  var prompt = ''

  if (mode === 'profile') {
    prompt = 'אתה מומחה לתזונה, NLP ואורח חיים בריא. קיבלת את הפרופיל המלא של המטופל/ת ' + name + '.\n\n' +
      'פרטי הפרופיל:\n' + JSON.stringify(profile, null, 2) + '\n\n' +
      'ספק ניתוח מקצועי ומעמיק בעברית עם המבנה הבא:\n\n' +

      '# 🧠 ניתוח NLP ומעגל השינוי\n' +
      '## שלב במעגל השינוי\n' +
      '(זהה: טרום הרהור / הרהור / הכנה / פעולה / שמירה — עם הסבר מדוע)\n\n' +
      '## רמות לוגיות\n' +
      '(נתח: סביבה / התנהגות / יכולות / ערכים / זהות / ייעוד — איפה הבעיה המרכזית)\n\n' +
      '## אמונות מגבילות\n' +
      '(זהה 2-3 אמונות מגבילות מהתשובות וכיצד להתמודד איתן)\n\n' +
      '## קווים זוהרים\n' +
      '(מה לא לגעת בו עדיין — מה עלול לגרום להתנגדות)\n\n' +
      '## אסטרטגיית הגישה המומלצת\n' +
      '(איך לגשת למטופל/ת — סגנון, קצב, דגשים)\n\n' +

      '---\n\n' +

      '# 🥗 המלצות תזונה אישיות\n' +
      '(בהתאם להרגלים, הגבלות, מטרות, בדיקות דם — המלץ על תמהיל ספציפי)\n\n' +

      '---\n\n' +

      '# 🩸 בדיקות דם — ניתוח והמלצות\n' +
      '## ערכים חריגים\n' +
      '(פרט כל ערך חריג ומשמעותו)\n\n' +
      '## המלצות לרופא\n' +
      '(מה לבדוק / לטפל / תוספים לשקול)\n\n' +
      '## שילובי מזון מומלצים\n' +
      '(אילו מזונות יעזרו לאיזן את הערכים)\n\n' +

      '---\n\n' +

      '# 🏃 תמהיל ספורט מומלץ\n' +
      '(בהתאם למטרות, כאבים, רמת פעילות נוכחית — המלץ על סוג, תדירות, עצימות)\n\n' +

      '---\n\n' +

      '# 🎯 סדר עדיפויות לטיפול\n' +
      '(3-5 דברים לטפל בהם ראשון — לפי חשיבות ודחיפות)'

  } else {
    prompt = 'אתה מאמן תזונה ואורח חיים דיגיטלי חם, מעצים ומקצועי באפליקציית "בין הראש לצלחת".\n\n' +
      'תפקידך לנתח את נתוני התזונה של ' + name + ' ולספק פידבק קצר, מעורר השראה ופרקטי.\n\n' +
      'חוקים חשובים לסגנון:\n' +
      '- ללא טבלאות או גרפים טקסטואליים\n' +
      '- ללא שפה שיפוטית: אין "קריטי", "בעיה", "נכשל", "יום רעב"\n' +
      '- ללא סימני אזהרה: אין 🚨 🔴 ⚠️\n' +
      '- קצר, סרוק, קל לקריאה במובייל\n' +
      '- תמיכה מנטלית לצד פרקטיקה תזונתית\n\n' +
      'מבנה הפלט:\n\n' +
      '# 🌟 נקודות אור\n' +
      '(2-3 שורות מפרגנות)\n\n' +
      '---\n\n' +
      '# 🎯 איך נדייק את הצלחת?\n' +
      '(מה כדאי להוסיף בצורה חיובית)\n\n' +
      '---\n\n' +
      '# 💡 3 רעיונות קטנים למחר\n' +
      '(3 טיפים פרקטיים)\n\n' +
      'יומן תזונה:\n' + logs
  }

  const message = await client.messages.create({
    model: 'claude-opus-4-5',
    max_tokens: 2000,
    messages: [{ role: 'user', content: prompt }]
  })

  return Response.json({ result: message.content[0].text })
}
