import Anthropic from '@anthropic-ai/sdk'

const client = new Anthropic({ apiKey: process.env.ANTHROPIC_API_KEY })

export async function POST(request) {
  const body = await request.json()
  const { name, logs, mode, profile, foodDiary } = body

  let prompt = ''

  if (mode === 'profile' && profile) {
    const p = profile

    prompt = `אתה אתי אטל — יועצת בריאות במגמת תזונה בגישת NLP. אתה מכינה ניתוח אישי עמוק ומקיף עבור ${name}.

הנחיה חשובה: כתבי בגוף שני נקבה (את/שלך), בעברית פשוטה וחמה, כאילו את יושבת מולה ומדברת אליה ישירות. הניתוח צריך להרגיש costume-made — שהיא תרגיש שראית אותה לעומק.

===== נתוני הלקוחה =====

פרטים אישיים:
- גיל: ${p.age || 'לא צוין'} | משקל: ${p.weight || 'לא צוין'} | גובה: ${p.height || 'לא צוין'} | מגדר: ${p.gender || 'לא צוין'}
- מטרה: ${p.goal || 'לא צוינה'} | משקל יעד: ${p.target_weight || 'לא צוין'} | פעילות: ${p.activity || 'לא צוינה'}

בריאות ורפואה:
- שינה: ${p.sleep_quality || 'לא צוין'} | בעיות שינה: ${p.sleep_issues || 'אין'} | שעת קימה: ${p.wake_time || 'לא צוינה'} | שעת שינה: ${p.sleep_time || 'לא צוינה'}
- עיכול: ${p.digestion || 'לא צוין'} | מחזור: ${p.menstrual_cycle || 'לא צוין'} | ימי דימום: ${p.menstrual_days || 'לא צוין'}
- מעשן/ת: ${p.smoking ? 'כן' : 'לא'} | תרופות ותוספים: ${p.medications || 'אין'} | מטפלים: ${p.therapists || 'אין'}
- היסטוריה רפואית: ${p.medical_history || 'לא צוינה'} | בריאות הורים: ${p.family_history || 'לא צוינה'}

הרגלי תזונה:
- הגבלות: ${p.diet_restrictions || 'אין'} | רגישויות: ${p.food_sensitivities || 'אין'} | נמנעת מ: ${p.avoided_foods || 'אין'}
- בוקר: ${p.breakfast_habits || 'לא צוין'} | צהריים: ${p.lunch_habits || 'לא צוין'} | ערב: ${p.dinner_habits || 'לא צוין'}
- נשנושים: ${p.snack_habits || 'לא צוין'} | בישול בבית: ${p.cooks_at_home ? 'כן' : 'לא'} | מסעדות בשבוע: ${p.restaurants_per_week || 'לא צוין'}

אורח חיים ורגש:
- מים ביום: ${p.water_intake || 'לא צוין'} | קפה: ${p.coffee_intake || 'לא צוין'} | אלכוהול: ${p.alcohol_intake || 'לא צוין'}
- אכילה רגשית: ${p.emotional_eating || 'לא צוין'} | שעות עבודה: ${p.work_hours || 'לא צוין'}
- רמת לחץ: ${p.stress_level || 'לא צוינה'}/10 | רמת אנרגיה: ${p.energy_level || 'לא צוינה'}/10 | מצב רוח: ${p.mood_notes || 'לא צוין'}

פעילות גופנית:
- סוג ותדירות: ${p.exercise_type || 'לא צוין'} | כאבים: ${p.pain_issues || 'אין'}

NLP ומטרות:
- מה רוצה להשיג: ${p.main_goal || 'לא צוין'}
- מה מעכב: ${p.goal_obstacles || 'לא צוין'}
- מוטיבציה: ${p.goal_motivation || 'לא צוינה'}/10
- איך תיראה ההצלחה: ${p.success_vision || 'לא צוין'}
- מה חשוב לה: ${p.important_values || 'לא צוין'}
- אירועים חיוביים: ${p.positive_memories || 'לא צוין'}

בדיקות דם (אם קיימות):
${JSON.stringify(p.blood_tests || {}, null, 2)}

${foodDiary ? `===== 5 ימי אכילה חופשיים לפני התהליך =====
${foodDiary}` : ''}

===== הנחיות לניתוח =====

צרי ניתוח מקיף ומעמיק הכולל את הסעיפים הבאים:

---

🌟 **${name} — הניתוח האישי שלך**

**מה שראיתי בך — הקווים הזוהרים**
[2-3 חוזקות אמיתיות שעולות מהנתונים. היי ספציפית — לא "את חזקה" אלא למה]

**המצב התזונתי שלך היום**
${foodDiary ? '[על בסיס 5 ימי האכילה: ממוצע פחמימות/חלבון/שומן באחוזים. מה בולט, מה חסר, מה עודף]' : '[על בסיס הרגלי האכילה שתיארת]'}

**ההשלכות הפיזיולוגיות של אורח החיים הנוכחי**
[קשרים ספציפיים: למשל — "כשאת לא שותה מספיק מים זה מאט את חילוף החומרים שלך ב-X, ובגיל שלך זה קריטי כי..." / "חוסר חלבון גורם לגוף לפרק שריר, וזה מסביר למה..." — היי פיזיולוגית ומדויקת]

**המחסומים האמיתיים שלך**
[לא "אין לך כוח רצון" — אלא הסבר מה באמת קורה. קשרים בין לחץ/שינה/אכילה רגשית/הרגלים]

**הרמות הלוגיות — איפה השינוי צריך לקרות**
[על פי דילטס:
- סביבה: מה בסביבה שלך מקשה/מקל
- התנהגות: מה את עושה בפועל
- יכולות: מה את יודעת/יכולה
- אמונות: מה את מאמינה על עצמך ועל האוכל
- זהות: מי את חושבת שאת בהקשר הזה]

**מה אומרות הבדיקות שלך**
[רק אם יש ערכים — פרשנות בשפה פשוטה. מה גבוה/נמוך/גבולי ומה המשמעות בחיי היומיום שלה]

**המלצות תזונה אישיות — בדיוק בשבילך**
[ספציפיות לפרופיל שלה — בהתחשב בבריאות, הגבלות, הרגלים, בדיקות]

**תוספים ובדיקות לשקול**
[על בסיס כל הנתונים — מה חסר, מה לבדוק]

**3 צעדים ראשונים — השבוע**
[קונקרטיים, ריאליסטיים, מותאמים לחיים שלה]

**מסר אישי ממני אליך**
[חם, אמיתי, מעצים — שהיא תרגיש שמישהי באמת ראתה אותה]

---
חשוב מאוד: היי ספציפית ואישית לכל אורך הניתוח. השתמשי בנתונים האמיתיים שלה, לא בכלליות.`

  } else if (logs) {
    prompt = `אתה אתי אטל — יועצת בריאות במגמת תזונה בגישת NLP.
נתחי את יומן התזונה השבועי של ${name} וצרי משוב חם, אישי ומעשי בעברית.

יומן תזונה:
${logs}

צרי משוב שכולל:

📊 **סיכום השבוע של ${name}**
[דפוסים, מה חזר, מה בולט]

✅ **מה עשית נהדר**
[הצלחות קונקרטיות]

⚡ **איפה את נתקעת**
[קשיים שעולים מהנתונים — בלי שיפוט]

🎯 **3 דברים לשבוע הבא**
[קונקרטיים ומעשיים]

💚 **מסר אישי**
[חם ומעצים]`
  }

  const message = await client.messages.create({
    model: 'claude-opus-4-5',
    max_tokens: 3000,
    messages: [{ role: 'user', content: prompt }]
  })

  return Response.json({ result: message.content[0].text })
}
